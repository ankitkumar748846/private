<p style="font-size: 19px;">&emsp;&emsp;<b>7 (F)</b>अगर कोई सदस्य रजिस्ट्रेशन करके या रजिस्ट्रेशन के बाद कुछ सहयोग करके 6 माह या उससे अधिक माह के लिए सहयोग छोड़ देता है तो उसे वैधानिक बनने के लिए 5 माह का लॉकिंग पीरियड तथा उस बीच के समस्त सहयोग पूर्ण करने होंगे।</p>

<p style="font-size: 19px;"><b>(यह नियम दिनांक 12/10/23  से लागू है|)</b></p>

     <p style="font-size: 19px;">&emsp;&emsp;<b>7 (G)</b> किसी अन्य व्यस्तता, पारिवारिक व्यस्तता, समारोह, कार्यक्रम आदि अन्य स्थितियों स्वयं या पारिवारिक आदि की स्थिति में सहयोग छूट जाने की दशा में दावा मान्य नही होगा, इस हेतु क्रमिक सहयोग करके वैधानिकता बहाल करने की व्यवस्था/ 10 सहयोग के बाद 90% अवसरों में सहयोग की स्थिति सम्बन्धी नियम लागू होगा।</p>

<p style="font-size: 19px;"><b>(यह नियम स्थापना के समय से प्रभावी है)</b></p>

<p style="font-size: 19px;"><b>8)</b>  कम से कम 10 सहयोग हो जाने के बाद यह देखा जा सकेगा की किसी सदस्य ने 90% अवसरों पर यानी 10 में से 09 बार कम से कम यदि सहयोग किया है, तो उसे किसी 01 का सहयोग न कर पाने के कारण उसकी सदस्यता न तो निलंबित होगी, न ही उसे सहयोग से इंकार किया जायेगा। बशर्ते सहयोग न कर पाने का वाजिब संतोषजनक कारण हो। इस प्रकार की छूट सहयोगों की कुल संख्या बढ़ने यानी10 बार 20बार, 50बार  के साथ साथ विचाराधीन रहेगी और उसके सहयोग के प्रतिशत में 80% से 90% तक विचार किया जा सकेगा। लेकिन 10सहयोग के बाद ही 90% का नियम अगले संशोधन तक लागू माना जायेगा। 10 सहयोग से पहले 90% अवसरों पर सहयोग के नियम को नही माना जायेगा। तब तक समस्त सहयोग करना अनिवार्य होगा।</p>

<p style="font-size: 19px;"><b>(यह नियम स्थापना के समय से लागू है)</b></p>

<p style="font-size: 19px;"><b>90% सहयोग की व्याख्या– अगर किसी ऐसे सदस्य की मृत्यु होती है जो की टीम से लंबे समय से जुड़ा था किंतु कुछ सहयोग के कारण अवैधानिक हो रहा है तो उस समय देखा जायेगा की मृत्यु की तिथि से 2 वर्ष पूर्व के बीच हुए कुल सहयोग का अगर 90% सहयोग किया है तो उसे 90% सहयोग के दायरे के वैधानिक मानते हुए सहयोग किया जाएगा।</b></p>

      <p style="font-size: 19px;">&emsp;&emsp;<b>8 (A)</b> किसी शिक्षक के सदस्य बनने के 10 सहयोग पूरे करने के उपरांत प्रति वर्ष अपरिहार्य स्थिति में 01 सहयोग न कर  पाने की की दशा में केवल 01 छूट दी जा सकेगी, लेकिन पूर्व में उसके द्वारा सदस्य बनने के बाद 10 सहयोग किया गया हो।</p>

<p style="font-size: 19px;"><b>(यह नियम स्थापना के समय से लागू है)</b></p>

      <p style="font-size: 19px;">&emsp;&emsp;<b>8 (B)</b>  यदि कोई शिक्षक/ सदस्य पूर्व में सभी सहयोग कर रहा था और वैधानिक सदस्य था किंतु गतिमान सहयोग के दौरान (सहयोग शुरू होने की तिथि से सहयोग खत्म होने तक) सहयोग तिथि समाप्त होने से पूर्व सहयोग नही किया रहता और उसी दौरान उसकी दुःखद मृत्यु हो जाती है तो वह लाभ का पात्र माना जायेगा क्योंकि यह माना जायेगा कि वह जीवित होते तो पूर्व की भाँति सहयोग करते। किंतु अगर सहयोग समाप्त हो जाने के बाद मृत्यु होती है तो गतिमान सहयोग की छूट का लाभ नही दिया जा सकेगा। जैसे-  सहयोग शुरू होने के पूर्व या शुरू होने के दिन या गतिमान सहयोग के दौरान कोई सदस्य हॉस्पिटल में भर्ती होता है और उसकी मृत्यु हो जाती है तो उस स्तिथि में दिवंगत शिक्षक के परिवार को सहयोग किया जा सकेगा। यहां पर यह भी देखा जाएगा कि उस स्थिति में गतिमान सहयोग पूर्ण गया, उस स्थिति में सहयोग करना जरूरी होगा। सहयोग समाप्त हो जाने के बाद मृत्यु होने की स्थिति में सहयोग नही दिया जा सकेगा।</p>
<p style="font-size: 19px;"><b>यह नियम सिर्फ शिक्षक के स्वयं के अपरिहार्य हालात जैसे गम्भीर दुर्घटना/ बीमारी/अस्पताल में होने की स्थिति में ही मान्य होगा।</b> </p>

<p style="font-size: 19px;">(बिंदु 8 (B) का नियम पूर्व में नही लागू था, पूर्व में बिंदु 8(B) को छोड़कर समस्त नियम और शर्तों के अधीनसहयोग करना था। सदस्यो की मांग और आवश्यकता को देखते हुए नियमो में शिथिलता प्रदान करने हेतु कोर टीम और जिला टीम की सहमति से बिंदु 8(B) को दिनाँक 16मई 2021 से जोड़कर प्रभावी किया जा रहा है।)</p>

<p style="font-size: 19px;">इसके अलावा जहां तक सहयोग की बात है, तो कोई भी किसी का भी सहयोग कर सकता है। कोई किसी का सहयोगात्मक भाव से गूगल फार्म भर सकता है।</p>

<p style="font-size: 19px;"><b>9)</b>  सुसाइड या किसी विवादित केस या अन्य केस जो संज्ञान लेने लायक हो, में कोर टीम के पास पड़ताल करके वस्तु स्थिति से अवगत होने के बाद निर्णय लेने का अधिकार होगा। आवश्यकता पड़ने पर वोटिंग या जिला इकाई या सदस्यों की राय भी ली जा सकेगी।</p>

     <p style="font-size: 19px;">&emsp;&emsp;<b>9 (A)</b> वस्तुतः एक से अधिक शिक्षक/ सदस्य की मृत्यु होने पर उसकी मृत्यु की तिथि के क्रम में ही सहयोग किया जाएगा। किंतु यदि किन्ही दो या अधिक शिक्षकों की मृत्यु एक ही तिथि में होती है तो ऐसी स्थिति में उस शिक्षक का सहयोग पहले किया जाएगा जिसके सहयोग करने का प्रतिशत/एवरेज अधिक होगा। उसके बाद अन्य का। उपरोक्त प्रकरणों में किसी विशेष परिस्थिति जैसे स्थलीय निरीक्षण न हो पाना, कुछ तकनीकी कमी आदि मामलों में कोर टीम सहयोग के क्रम का निर्णय अपने विवेकानुसार ले सकेगी।</p>

     <p style="font-size: 19px;">&emsp;&emsp;<b>9 (B)</b> नॉमिनी सम्बन्धी विवाद की स्थिति में प्रदेश / कोर टीम परीक्षणोपरांत निर्णय लेने और सहयोग करवाने हेतु स्वतंत्र होगी।</p>

<p style="font-size: 19px;"><b>&emsp;&emsp;(यह नियम स्थापना के समय से प्रभावी है।)</b></p>

<p style="font-size: 19px;"><b>10)</b>  उपरोक्त नियमो से इतर हटकर न किसी का सहयोग किया जा सकेगा जा सकेगा न ही किसी का सहयोग रद्द किया जा सकेगा।</p>

<p style="font-size: 19px;"><b>&emsp;&emsp;(यह नियम स्थापना के समय से ही लागू है)</b></p>

   <p style="font-size: 19px;">&emsp;&emsp;<b>10 (A)</b> अनुशासन हीनता, TSCT  विरोधी गतिबिधि या किसी प्रकार की साजिश करने वालो के विरुद्ध TSCT कोर टीम के पास निर्णय लेने का अधिकार होगा। कोई भी शिक्षक tsct के साथ साथ अन्य समान प्रकार के संघो/टीम में सदस्य के रुप में रह सकता है, किंतु किसी अन्य सम विषयक टीम के पदाधिकारी के रूप में कोई शिक्षक tsct का वैध मेम्बर प्रथम दृष्टया नही होगा (इस प्रकरण पर कोर टीम निर्णय लेने हेतु स्वतंत्र होगी)
कोई भी व्यक्ति/ शिक्षक या अन्य टीचर्स सेल्फ केयर टीम / समिति के खिलाफ दुष्प्रचार या अफवाह फैलाता है, बिना सबूत या आंकड़े प्रस्तुत किये आरोप लगाता है तो टीम उसके विरुद्ध किसी भी प्रकार की कार्यवाही करने हेतु स्वतंत्र होगी।</p>

<p style="font-size: 19px;"><b>&emsp;&emsp;(यह नियम स्थापना के समय से लागू है)</b> </p>

     <p style="font-size: 19px;">&emsp;&emsp;<b>10 (B)</b> टेलीग्राम ग्रुप पर जानकारी हेतु समस्त सूचनाएं समय समय से प्रदान की जाती हैं। कोई सदस्य  टेलीग्राम ग्रुप से सूचनाएं नहीं प्राप्त करता तो वह स्वयं जिम्मेदार होगा। </p>

<p style="font-size: 19px;"><b>&emsp;&emsp;(यह नियम स्थापना के समय से लागू है)</b></p>

    <p style="font-size: 19px;">&emsp;&emsp;<b>10 (C)</b>  सदस्य हेल्पलाइन के माध्यम से अपना सवाल जवाब प्राप्त कर सकेंगे।</p>

<p style="font-size: 19px;"><b>&emsp;&emsp;(यह नियम स्थापना के समय से लागू है)</b></p>

   <p style="font-size: 19px;">&emsp;&emsp;<b>10 (D)</b> समय और आवश्यकता को देखते हुए TSCT के किसी भी नियमो में कभी भी संशोधन/परिवर्तन किया जा सकेगा। </p>

