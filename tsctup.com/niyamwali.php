   <p style="font-size: 19px;">&emsp;&emsp;<b>10 (E)</b> TSCT सहयोग सीधे दिवंगत परिवारों के नॉमिनी के खाते में करवाती है इसलिए किसी भी प्रकार की न्यायिक चुनौती देने का अधिकार किसी व्यक्ति या सदस्य  के पास नहीं होगा।</p>

<p style="font-size: 19px;"><b>&emsp;&emsp;(यह नियम स्थापना के समय से लागू है)</b></p>


<p style="font-size: 19px;"><b>11)</b> TSCT किसी भी शिक्षक  को जबरन या दबाव देकर सदस्य नहीं बनाती है, सदस्यों को नियम स्वीकार करके ही सदस्य बनने का विकल्प प्रदान किया जाता है, स्वेच्छा से कोई भी सदस्य कभी भी खुद को अलग कर सकता है।</p>

